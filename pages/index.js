import Head from "next/head";
import Image from "next/image";
import Button from "../components/Button";
import styles from "../styles/Home.module.css";
import { useState, useEffect } from "react";
import useUser from "../hooks/use-user";

export default function Home() {
  const [user, fetchUser] = useState(null);

  const { data, isLoading } = useUser();
  const songs = user ? user.tracks : data?.tracks;

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <h1>Top40.fm</h1>
      <div className="Access220_32x32_4"></div>

      <Button fetchUser={fetchUser} />
      {isLoading ? (
        "loading..."
      ) : (
        <>
          <h1>
            {user ? "These are your" : "These are my"} Top 40 songs this year.
          </h1>
          {songs.map((song, index) => {
            return (
              <article key={song.id}>
                <Image
                  alt={song.album}
                  src={song.albumImg.url}
                  width={350}
                  height={350}
                />
                <h2>
                  <a href={song.songUrl}>{song.title}</a>
                </h2>
                <p>
                  {song.artist} | {song.album}
                </p>
              </article>
            );
          })}
        </>
      )}
    </div>
  );
}
